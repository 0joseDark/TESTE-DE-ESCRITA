# Descrição: Este script demonstra operações matemáticas básicas em Python.

# Definimos duas variáveis para usar nos exemplos
a = 10
b = 3

# Adição
soma = a + b
print("Adição:", a, "+", b, "=", soma)   # Exibe o resultado da soma

# Subtração
subtracao = a - b
print("Subtração:", a, "-", b, "=", subtracao)   # Exibe o resultado da subtração

# Multiplicação
multiplicacao = a * b
print("Multiplicação:", a, "*", b, "=", multiplicacao)   # Exibe o resultado da multiplicação

# Divisão (resultado em número decimal)
divisao = a / b
print("Divisão:", a, "/", b, "=", divisao)   # Exibe o resultado da divisão

# Divisão inteira (apenas a parte inteira do resultado)
divisao_inteira = a // b
print("Divisão inteira:", a, "//", b, "=", divisao_inteira)   # Exibe apenas o quociente inteiro

# Resto da divisão (módulo)
resto = a % b
print("Resto da divisão:", a, "%", b, "=", resto)   # Exibe o resto da divisão

# Potência (a elevado a b)
potencia = a ** b
print("Potência:", a, "**", b, "=", potencia)   # Exibe a potência (10^3 = 1000)
