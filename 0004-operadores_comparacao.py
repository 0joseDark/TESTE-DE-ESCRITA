# Descrição: Este script demonstra operadores de comparação em Python.

# Definimos duas variáveis para comparar
a = 10
b = 5

print("Valores usados: a =", a, " e b =", b)
print("----------------------------------")

# Igualdade (==) → verifica se os valores são iguais
print("a == b :", a == b)   # False, porque 10 não é igual a 5

# Diferença (!=) → verifica se os valores são diferentes
print("a != b :", a != b)   # True, porque 10 é diferente de 5

# Maior que (>) → verifica se o primeiro valor é maior que o segundo
print("a > b  :", a > b)    # True, porque 10 é maior que 5

# Menor que (<) → verifica se o primeiro valor é menor que o segundo
print("a < b  :", a < b)    # False, porque 10 não é menor que 5

# Maior ou igual (>=) → verifica se o primeiro valor é maior ou igual ao segundo
print("a >= b :", a >= b)   # True, porque 10 é maior que 5

# Menor ou igual (<=) → verifica se o primeiro valor é menor ou igual ao segundo
print("a <= b :", a <= b)   # False, porque 10 não é menor nem igual a 5
